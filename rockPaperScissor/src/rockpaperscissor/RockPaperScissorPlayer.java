package rockpaperscissor;



/**
 *
 * @author kendrabooker
 */
public class RockPaperScissorPlayer {
    public static String name;
    public static int id, count, rounds;
    
    public RockPaperScissorPlayer() {
    
    }
    
    public RockPaperScissorPlayer(String name, int rounds) {
        this.name = name;
        this.rounds = rounds;
        id = count;
        count++;
    }
    
    
    
    
    
}
